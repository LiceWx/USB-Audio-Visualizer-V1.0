void OLED_ShowString(uint8_t x, uint8_t y, const char *str, uint8_t size, uint8_t mode)
{
  while (*str)
  {
    OLED_ShowChar(x, y, *str, size, mode);
    x += (size == 8) ? 6 : (size / 2);
    str++;
  }
}

static uint32_t OLED_Pow(uint8_t m, uint8_t n)
{
  uint32_t result = 1;
  while (n--) result *= m;
  return result;
}

void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len, uint8_t size, uint8_t mode)
{
  uint8_t offset = (size == 8) ? 2 : 0;
  for (uint8_t t = 0; t < len; t++)
  {
    uint8_t digit = (uint8_t)((num / OLED_Pow(10, len - t - 1)) % 10);
    OLED_ShowChar(x + (size / 2 + offset) * t, y, (char)('0' + digit), size, mode);
  }
}

/* ── Progress bar: y0..y1 (vertical range), percent 0-100 fills left→right ── */
void OLED_DrawBar(uint8_t y0, uint8_t y1, uint8_t percent)
{
  uint8_t w = (uint8_t)(((uint16_t)percent * 128U) / 100U);
  if (w > 128) w = 128;

  for (uint8_t x = 0; x < 128; x++)
  {
    for (uint8_t y = y0; y <= y1; y++)
    {
      OLED_DrawPoint(x, y, (x < w) ? 1 : 0);
    }
  }
}

/* ── Time-domain waveform: draw count samples from buf[start], scaled to y0..y1 ── */
void OLED_DrawWave(const uint16_t *buf, uint16_t start, uint16_t count, uint8_t y0, uint8_t y1, uint8_t gain)
{
  uint8_t mid  = (y0 + y1) / 2U;
  uint8_t amp  = (y1 - y0) / 2U;   /* max pixels from center (half height) */
  float   step = (float)count / 128.0f;

  for (uint8_t col = 0; col < 128; col++)
  {
    uint16_t idx = start + (uint16_t)((float)col * step);
    if (idx >= start + count) idx = start + count - 1U;

    /* uint16 [0..65535] → signed, apply gain, clamp to ±amp */
    int16_t val  = (int16_t)(buf[idx] - 32768U);
    int32_t scaled = ((int32_t)val * (int32_t)gain) / 8;   /* gain/8 scaling */
    int16_t yoff = (int16_t)((scaled * (int32_t)amp) / 32768);
    int16_t y    = (int16_t)mid - yoff;

    if      (y < (int16_t)y0) y = y0;
    else if (y > (int16_t)y1) y = y1;

    OLED_DrawPoint(col, (uint8_t)y, 1);
  }
}

/* ── Spectrogram: 24 pooled bins, each 5px wide = 120 cols, centered ── */
/* mel_data[24] = per-bin magnitude, max_val = normalization max          */
void OLED_DrawSpectrogram(const float *mel_data, float max_val)
{
  float scale;
  if (max_val < 0.00001f)
    scale = 0.0f;
  else
    scale = 48.0f / max_val;  /* 48 pixels max height */

  /* Blue zone: pages 2..7 (rows 16..63) */
  for (uint8_t page = 2; page <= 7; page++)
  {
    uint8_t row_base = page * 8U;

    for (uint8_t col = 0; col < 128; col++)
    {
      uint8_t byte_val = 0U;

      /* Map column to bin: 4px bin + 1px gap, left-aligned */
      {
        uint16_t stride = SPECTRO_BIN_WIDTH + SPECTRO_GAP;
        uint16_t pos    = col % stride;
        if (pos >= SPECTRO_BIN_WIDTH)
        {
          OLED_GRAM[col][page] = 0U;  /* gap column */
          continue;
        }
        uint16_t bin = (uint16_t)(col / stride);
        if (bin >= POOLED_BINS)
        {
          OLED_GRAM[col][page] = 0U;
          continue;
        }

        float hf = mel_data[bin] * scale;
        uint8_t h;
        if (hf > 48.0f) h = 48U;
        else if (hf < 0.0f) h = 0U;
        else h = (uint8_t)hf;

        int16_t bar_top = (int16_t)(63U - h + 1U);
        if (bar_top < 16) bar_top = 16;
        if (bar_top > 64) bar_top = 64;

        for (uint8_t bit = 0; bit < 8; bit++)
        {
          uint8_t row = row_base + bit;
          if (row >= (uint8_t)bar_top && row <= 63U)
            byte_val |= (1U << bit);
        }

        OLED_GRAM[col][page] = byte_val;
      }
    }
  }
}

/* ── Onset Detection Function bar chart: 128 columns, filled from bottom ── */
void OLED_DrawODFBars(const float *odf, float max_val)
{
  float scale;
  if (max_val < 0.00001f)
    scale = 0.0f;
  else
    scale = 48.0f / max_val;  /* 48 pixels max in blue zone */

  for (uint8_t page = 2; page <= 7; page++)
  {
    uint8_t row_base = page * 8U;

    for (uint8_t col = 0; col < 128; col++)
    {
      float hf = odf[col] * scale;
      uint8_t h;
      if (hf > 48.0f) h = 48U;
      else if (hf < 1.0f) h = 1U;   /* minimum 1 px — zero is still visible */
      else h = (uint8_t)hf;

      int16_t bar_top = (int16_t)(63U - h + 1U);
      if (bar_top < 16) bar_top = 16;
      if (bar_top > 64) bar_top = 64;

      uint8_t byte_val = 0U;
      for (uint8_t bit = 0; bit < 8; bit++)
      {
        uint8_t row = row_base + bit;
        if (row >= (uint8_t)bar_top && row <= 63U)
          byte_val |= (1U << bit);
      }

      OLED_GRAM[col][page] = byte_val;
    }
  }
}

/* ── Draw external bitmap into GRAM at specified pages ── */
void OLED_DrawBitmap(uint8_t start_page, uint8_t num_pages, const uint8_t bitmap[][6])
{
  for (uint8_t page = 0; page < num_pages; page++)
  {
    for (uint8_t col = 0; col < 128; col++)
    {
      OLED_GRAM[col][start_page + page] = bitmap[col][page];
    }
  }
}

void OLED_Init(void)
{
  /* Set initial pin states (pins are already GPIO outputs from MX_GPIO_Init) */
  OLED_SCK_Clr();
  OLED_MOSI_Clr();
  OLED_DC_Set();

  OLED_RES_Clr();
  osDelay(10);
  OLED_RES_Set();
  osDelay(10);

  OLED_WR_Byte(0xAE, OLED_CMD); /* display off */
  OLED_WR_Byte(0x00, OLED_CMD);
  OLED_WR_Byte(0x10, OLED_CMD);
  OLED_WR_Byte(0x40, OLED_CMD);
  OLED_WR_Byte(0x81, OLED_CMD); /* contrast */
  OLED_WR_Byte(0xCF, OLED_CMD);
  OLED_WR_Byte(0xA1, OLED_CMD); /* segment remap */
  OLED_WR_Byte(0xC8, OLED_CMD); /* COM scan direction */
  OLED_WR_Byte(0xA6, OLED_CMD); /* normal display */
  OLED_WR_Byte(0xA8, OLED_CMD); /* multiplex */
  OLED_WR_Byte(0x3F, OLED_CMD); /* 1/64 duty */
  OLED_WR_Byte(0xD3, OLED_CMD); /* display offset */
  OLED_WR_Byte(0x00, OLED_CMD);
  OLED_WR_Byte(0xD5, OLED_CMD); /* clock div */
  OLED_WR_Byte(0x80, OLED_CMD);
  OLED_WR_Byte(0xD9, OLED_CMD); /* pre-charge */
  OLED_WR_Byte(0xF1, OLED_CMD);
  OLED_WR_Byte(0xDA, OLED_CMD); /* COM pins */
  OLED_WR_Byte(0x12, OLED_CMD);
  OLED_WR_Byte(0xDB, OLED_CMD); /* VCOMH */
  OLED_WR_Byte(0x40, OLED_CMD);
  OLED_WR_Byte(0x20, OLED_CMD); /* addressing mode */
  OLED_WR_Byte(0x02, OLED_CMD); /* page addressing */
  OLED_WR_Byte(0x8D, OLED_CMD); /* charge pump */
  OLED_WR_Byte(0x14, OLED_CMD);
  OLED_WR_Byte(0xA4, OLED_CMD);
  OLED_WR_Byte(0xA6, OLED_CMD);
  OLED_Clear();
  OLED_WR_Byte(0xAF, OLED_CMD); /* display on */
}

