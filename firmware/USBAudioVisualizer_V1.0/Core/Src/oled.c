/* ── 128x64 GRAM buffer ── */
static uint8_t OLED_GRAM[128][8];

/* ── Direct BSRR masks (all on GPIOA) ── */
/* PA4=DC, PA5=SCK, PA6=MOSI, PA7=RES */
#define OLED_DC_BIT    4U
#define OLED_SCK_BIT   5U
#define OLED_MOSI_BIT  6U
#define OLED_RES_BIT   7U

#define OLED_DC_Set()   (GPIOA->BSRR = (1U << OLED_DC_BIT))
#define OLED_DC_Clr()   (GPIOA->BSRR = (1U << (OLED_DC_BIT + 16U)))
#define OLED_RES_Set()  (GPIOA->BSRR = (1U << OLED_RES_BIT))
#define OLED_RES_Clr()  (GPIOA->BSRR = (1U << (OLED_RES_BIT + 16U)))
#define OLED_SCK_Set()  (GPIOA->BSRR = (1U << OLED_SCK_BIT))
#define OLED_SCK_Clr()  (GPIOA->BSRR = (1U << (OLED_SCK_BIT + 16U)))
#define OLED_MOSI_Set() (GPIOA->BSRR = (1U << OLED_MOSI_BIT))
#define OLED_MOSI_Clr() (GPIOA->BSRR = (1U << (OLED_MOSI_BIT + 16U)))

/* ── Software SPI byte write (bit-banged, BSRR direct) ── */
__attribute__((always_inline))
static inline void OLED_WR_Byte(uint8_t dat, uint8_t cmd)
{
  if (cmd) OLED_DC_Set();
  else     OLED_DC_Clr();

  /* Unrolled bit-bang with BSRR; ~3 stores/bit → ~60ns/bit → ~17 MHz */
  #define OLED_BIT(n)                                    \
    if (dat & (0x80U >> (n))) {                          \
      OLED_MOSI_Set(); __NOP();                          \
      OLED_SCK_Set();  __NOP();                          \
      OLED_SCK_Clr();                                    \
    } else {                                             \
      OLED_MOSI_Clr(); __NOP();                          \
      OLED_SCK_Set();  __NOP();                          \
      OLED_SCK_Clr();                                    \
    }

  OLED_BIT(0); OLED_BIT(1); OLED_BIT(2); OLED_BIT(3);
  OLED_BIT(4); OLED_BIT(5); OLED_BIT(6); OLED_BIT(7);

  #undef OLED_BIT

  OLED_DC_Set();
}

/* ── Update display from GRAM ── */
void OLED_Refresh(void)
{
  for (uint8_t page = 0; page < 8; page++)
  {
    OLED_WR_Byte(0xB0 + page, OLED_CMD);   /* page start address */
    OLED_WR_Byte(0x00, OLED_CMD);           /* column low  */
    OLED_WR_Byte(0x10, OLED_CMD);           /* column high */
    for (uint8_t col = 0; col < 128; col++)
    {
      OLED_WR_Byte(OLED_GRAM[col][page], OLED_DATA);
    }
  }
}

void OLED_Clear(void)
{
  memset(OLED_GRAM, 0, sizeof(OLED_GRAM));
  OLED_Refresh();
}

/* ── Draw point: mode 0=dark, 1=bright, 2=toggle (XOR) ── */
static void OLED_DrawPoint(uint8_t x, uint8_t y, uint8_t mode)
{
  uint8_t page = y >> 3;
  uint8_t bit  = 1 << (y & 0x07);
  if (mode == 2U)
    OLED_GRAM[x][page] ^= bit;          /* XOR toggle */
  else if (mode)
    OLED_GRAM[x][page] |= bit;
  else
    OLED_GRAM[x][page] &= ~bit;
}

