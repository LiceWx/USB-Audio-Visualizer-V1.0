/* 6x8 font — forward declaration (defined below) */
extern const uint8_t asc2_0806[95][6];

void OLED_ShowChar(uint8_t x, uint8_t y, char chr, uint8_t size, uint8_t mode)
{
  uint8_t i, m, temp;
  uint8_t x0 = x, y0 = y;
  uint8_t idx = (uint8_t)(chr - ' ');
  if (idx > 94) return;

  uint8_t count = (size == 8U) ? 6U : size;   /* 6x8 font: 6 columns, not 8 */
  for (i = 0U; i < count; i++)
  {
    if (size == 8U)
      temp = asc2_0806[idx][i];
    else if (size == 16U)
      temp = asc2_1608[idx][i];
    else
      return;

    for (m = 0; m < 8; m++)
    {
      if (temp & 0x01)
        OLED_DrawPoint(x, y, mode);
      else if (mode != 2U)              /* mode=0/1 → draw background */
        OLED_DrawPoint(x, y, !mode);
      /* mode=2 (XOR): skip background pixels — let bar show through */
      temp >>= 1;
      y++;
    }
    x++;
    if ((size != 8) && ((x - x0) == size / 2))
    {
      x = x0;
      y0 += 8;
    }
    y = y0;
  }
}

